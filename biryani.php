    <article class="mb-4">
      <div class="container px-4 px-lg-5">
        <div class="row gx-4 gx-lg-5 justify-content-center">
          <div class="col-md-10 col-lg-8 col-xl-7">
            <p>
              Biryani is a dish loved by everyone from children's to older's.
            </p>
            <p>
              Biryani can be made using mutton,eggs,beef.Lets see the receipe of
              the most loved chicken biryani.
            </p>
            <h2 class="section-heading">Ingredients</h2>
            <ul>
              <li>
                <p>Rice 1kg</p>
              </li>
              <li><p>Chicken 1kg</p></li>
              <li><p>Onion 1/2kg,chopped</p></li>
              <li><p>Onion 1,sliced</p></li>
              <li><p>Tomato 1/2kg,sliced</p></li>
              <li><p>Yogurt 200g</p></li>
              <li><p>Ginger Paste 5tbs</p></li>
              <li><p>Garlic Paste 5tbs</p></li>
              <li><p>Green Chilli 5</p></li>
              <li><p>Coriander for decoration</p></li>
              <li><p>Mint leaves for decoration</p></li>
              <li>
                <p>Khada Garam Masala</p>
                <ul>
                  <li>Cloves 5/6</li>
                  <li>Black pepper 8/10</li>
                  <li>Cinnamon 5</li>
                  <li>Cumin Seeds</li>
                  <li>Green Cardamom 5</li>
                  <li>Black Cardamom 1</li>
                  <li>Star Anise 1</li>
                </ul>
              </li>
              <li><p>Any Cooking Oil 250g</p></li>
              <li><p>Salt to taste</p></li>
              <li><p>Suhana Biryani Masala(You can use your favorite)</p></li>
              <li><p>Red Chilli Powder</p></li>
              <li><p>Lemon 2</p></li>
            </ul>
            <h2 class="section-heading">Process</h2>
            <ul>
              <li>
                <p>
                  Take all chicken pieces and add to a bowl.Then add
                  yogurt,garlic paste 4tbs,ginger 4tbs,sliced tomato,salt to
                  taste,red chilli powder,oil 2tbs.Squeeze 1&1/2 lemon.
                </p>
              </li>
              <li>
                <p>
                  Mix everything weel and marinate the chicken.Cover and set
                  this aside for 1/2 hour.
                </p>
              </li>
              <li>
                <p>
                  Meanwhile add rice to a large pot and rinse it atleast
                  twice.Drain and soak in fresh water for 1 hour.
                </p>
              </li>
              <li>
                <p>
                  Take oil in a pot and fry the 1/2kg of chopped onion.After
                  frying grind it.
                </p>
              </li>
              <li>
                <b>Cooking Chicken</b>
                <ul>
                  <li>
                    <p>
                      Now,take a pot put 2/4tbs oil.Add marinated chicken and
                      biryani masala & saute it.After 10 minutes add grinded
                      fried onion.
                    </p>
                  </li>
                  <li>
                    <p>
                      Lower the flame completely.Cover and cook until the
                      chicken is soft,tender and completely cooked.
                    </p>
                  </li>
                  <li><p>Do not overcook the chicken</p></li>
                </ul>
              </li>
              <li>
                <p>
                  For cooking biryani take a larger pot.Add 4/5tbs oil.Then add
                  chopped onion,garlic paste 1tbs,ginger paste 1tbs,khada garam
                  masala.
                </p>
              </li>
              <li>
                <p>
                  On medium heat,fry them stirring often until light brown.Do
                  not burn or it will give bitter taste.
                </p>
              </li>
              <li>
                <p>
                  Layer drained rice in the pot.Add salt to taste.Now,add water
                  start with 1 glass.Stir and taste the water it must be
                  slightly salty.Pour water over the rice gently.(Water atleast
                  double than rice)
                </p>
              </li>
              <li>
                <p>
                  Cook on medium flame until the rice is done.Add the layer of
                  cooked chicken.
                </p>
              </li>
              <li>
                <p>Sprinkle some coriander and mint leaves for decoration.</p>
              </li>
              <li>
                <p>
                  Cover the pot with lid.Cook until the biryani rice is
                  perfectly done,grainy and fully cooked.Rest it covered for 10
                  minutes.
                </p>
              </li>
            </ul>
