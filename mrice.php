    <article class="mb-4">
      <div class="container px-4 px-lg-5">
        <div class="row gx-4 gx-lg-5 justify-content-center">
          <div class="col-md-10 col-lg-8 col-xl-7">
            <p>
              A fried rice is chinese dish of cooked rice with other ingredients
              like eggs,vegetables or meats.
            </p>
            <p>
              Lets try a Mushroom fried rice,which is a classic, flavorful and
              comforting Indian chinese style vegetarian fried rice dish made
              with mushrooms.
            </p>
            <h2 class="section-heading">Ingredients</h2>
            <ul>
              <li><p>Cooked Rice 3 cups</p></li>
              <li>
                <p>
                  Mushrooms 2 cup,cleaned,sliced,put into boiling water for 30
                  seconds
                </p>
              </li>
              <li><p>Green Capcicum 1,sliced</p></li>
              <li><p>Beans 5</p></li>
              <li><p>Onions 2,sliced</p></li>
              <li><p>Garlic 3,crushed</p></li>
              <li><p>Vinegar 1/2tsp</p></li>
              <li><p>Soy Sauce 3/4tsp</p></li>
              <li>
                <p>Pepper Powder 1tsp</p>
              </li>
              <li><p>Salt to taste</p></li>
              <li><p>Any Cooking Oil 4tsp</p></li>
            </ul>
            <h2 class="section-heading">Process</h2>
            <ul>
              <li>
                <p>
                  Heat oil in a wok or a wide cooking vessel. Once the oil turns
                  hot, add the minced garlic and saute for few secs.
                </p>
              </li>
              <li>
                <p>
                  Add the beans and saute on high flame for 3 mts, tossing the
                  contents constantly. Next add the capsicum and spring onion
                  whites and saute on high flame, tossing the contents
                  constantly for 2 mts.
                </p>
              </li>
              <li>
                <p>
                  Add the mushrooms and cook on high flame, tossing content
                  continuously for 3 mts. Add soy sauce and white vinegar and
                  mix.
                </p>
              </li>
              <li>
                <p>
                  Add the rice and mix well to combine. Keep mixing the contents
                  while cooking high flame. The vessel should be well greased so
                  that the rice does not stick to the vessel. Cook on high flame
                  for 3-4 mts continuously. Season with salt and pepper powder
                  and mix. Turn off flame.
                </p>
              </li>
              <li>
                <p>
                  Remove to a serving bowl. Garnish with spring onion greens and
                  serve with tomato ketchup or sweet and sour vegetables.
                </p>
              </li>
            </ul>
